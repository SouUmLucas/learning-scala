// Control flow

// if  statements
val name: String = "George"

if (name == "Sammy") {
  println("Your name is Sammy")
} else if (name == "George") {
  println("Your name is George")
} else {
  println("What is your name?")
}

// Logical operators
// AND 
println((1 == 2) && (2 == 2))
println((1 == 2) || (2 != 1))

// AND NOT
println(!(1 == 1))